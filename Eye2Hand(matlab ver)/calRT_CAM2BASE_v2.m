function RMSE = computeErro(A, B, para)
    R_B2C = angle2dcm(para(1),para(2),para(3));
    t_B2C = para(4:6);
    T_B2C = [R_B2C, t_B2C';0,0,0,1];
    delta = zeros(4*(size(A, 3)-1),4);
    for i=2:size(A, 3)
        delta((i-1)*4-3:(i-1)*4,:) = T_B2C*A(:,:,i)-B(:,:,i)*T_B2C;
        MSE = sum(sum(delta.^2))/numel(delta);
        RMSE = sqrt(MSE);
    end
end